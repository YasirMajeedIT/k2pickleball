function runSchema(PDO $pdo): array {
    $log = [];
    $schemaFile = K2_ROOT . '/database/schema.sql';
    $sql = file_get_contents($schemaFile);
    
    // Remove comments and split
    $sql = preg_replace('/--[^\n]*\n/', "\n", $sql);
    $statements = array_filter(array_map('trim', explode(';', $sql)), fn($s) => !empty($s));
    
    $ok = 0;
    $skip = 0;
    $errors = 0;
    foreach ($statements as $stmt) {
        try {
            $pdo->exec($stmt);
            $ok++;
            if (preg_match('/CREATE TABLE\s+(?:IF NOT EXISTS\s+)?`?(\w+)`?/i', $stmt, $m)) {
                $log[] = ['ok', "Created table: {$m[1]}"];
            }
        } catch (PDOException $e) {
            if (str_contains($e->getMessage(), 'already exists')) {
                $skip++;
            } else {
                $errors++;
                $log[] = ['error', $e->getMessage()];
            }
        }
    }
    $log[] = ['info', "Schema: {$ok} executed, {$skip} skipped, {$errors} errors"];
    return $log;
}

function runMigrations(PDO $pdo, string $host, string $port, string $user, string $pass, string $dbName): array {
    $log = [];
    $migrationsDir = K2_ROOT . '/database/migrations';
    
    if (!is_dir($migrationsDir)) {
        $log[] = ['warn', 'No migrations directory found — skipping'];
        return $log;
    }
    
    $files = glob($migrationsDir . '/*.sql');
    if (empty($files)) {
        $log[] = ['info', 'No migration files found'];
        return $log;
    }
    
    // Sort files naturally (001_, 002_, ..., then alphabetic for unnumbered)
    usort($files, function($a, $b) {
        $aBase = basename($a);
        $bBase = basename($b);
        $aNum = preg_match('/^(\d+)_/', $aBase, $am) ? (int)$am[1] : 9999;
        $bNum = preg_match('/^(\d+)_/', $bBase, $bm) ? (int)$bm[1] : 9999;
        if ($aNum !== $bNum) return $aNum - $bNum;
        return strcmp($aBase, $bBase);
    });
    
    // Use mysqli for multi_query support (handles SET @var, PREPARE/EXECUTE patterns)
    // Disable mysqli exception throwing so we can handle errors gracefully
    mysqli_report(MYSQLI_REPORT_OFF);
    
    $mysqli = new mysqli($host, $user, $pass, $dbName, (int)$port);
    if ($mysqli->connect_error) {
        $log[] = ['error', 'mysqli connect failed: ' . $mysqli->connect_error];
        return $log;
    }
    $mysqli->set_charset('utf8mb4');
    
    $ok = 0;
    $errors = 0;
    foreach ($files as $file) {
        $filename = basename($file);
        if ($filename === '.gitkeep' || str_starts_with($filename, '_')) continue;
        
        $sql = file_get_contents($file);
        // Strip single-line comments but preserve the SQL structure
        $sql = preg_replace('/--[^\n]*/', '', $sql);
        $sql = trim($sql);
        if (empty($sql)) continue;
        
        $fileOk = true;
        $fileErrors = [];
        
        try {
            $queryOk = $mysqli->multi_query($sql);
            if ($queryOk) {
                // Consume all result sets, checking each for errors
                do {
                    if ($result = $mysqli->store_result()) {
                        $result->free();
                    }
                    if ($mysqli->errno && !isIgnorableMigrationError($mysqli->error)) {
                        $fileOk = false;
                        $fileErrors[] = $mysqli->error;
                    }
                } while ($mysqli->more_results() && $mysqli->next_result());
            }
            
            // Check final error state
            if ($mysqli->errno && !isIgnorableMigrationError($mysqli->error)) {
                $fileOk = false;
                if (!in_array($mysqli->error, $fileErrors, true)) {
                    $fileErrors[] = $mysqli->error;
                }
            }
        } catch (\Throwable $e) {
            // Catch any mysqli exceptions that slip through
            $msg = $e->getMessage();
            if (!isIgnorableMigrationError($msg)) {
                $fileOk = false;
                $fileErrors[] = $msg;
            }
            // Drain remaining results so connection is clean
            while ($mysqli->more_results() && $mysqli->next_result()) {
                if ($result = $mysqli->store_result()) {
                    $result->free();
                }
            }
        }
        
        $ok++;
        if ($fileOk) {
            $log[] = ['ok', "Migration: {$filename}"];
        } else {
            $errors += count($fileErrors);
            foreach ($fileErrors as $err) {
                $log[] = ['error', "{$filename}: {$err}"];
            }
        }
    }
    
    $mysqli->close();
    $log[] = ['info', "Migrations: {$ok} files processed, {$errors} errors"];
    return $log;
}

function isIgnorableMigrationError(string $msg): bool {
    $patterns = [
        'already exists',
        'Duplicate column',
        'Duplicate entry',
        'Duplicate key',
        "Can't DROP",
        'Unknown column',
        'Column not found',
        'check that it exists',
        "doesn't exist",
        'Table already exists',
        'Foreign key constraint is incorrectly formed',
    ];
    foreach ($patterns as $p) {
        if (str_contains($msg, $p)) return true;
    }
    return false;
}

