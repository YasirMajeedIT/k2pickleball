            $installLog = array_merge($installLog, $migLog);
            
            // Step 5: Seed data
            $installLog[] = ['info', '── Step 5: Seeding database...'];
            $seedLog = runSeeder($pdo, $data['admin_email'], $data['admin_password']);
            $installLog = array_merge($installLog, $seedLog);
            
            // Step 6: Create lock file
            $lockData = json_encode([
                'installed_at' => date('c'),
                'php_version'  => PHP_VERSION,
                'admin_email'  => $data['admin_email'],
            ]);
            file_put_contents(K2_LOCK, $lockData, LOCK_EX);
            $installLog[] = ['ok', 'Installation lock created'];
            
            // Step 7: create storage subdirs if missing
            foreach (['storage/cache', 'storage/logs', 'storage/uploads'] as $dir) {
                $full = K2_ROOT . '/' . $dir;
                if (!is_dir($full)) {
                    mkdir($full, 0775, true);
                }
            }
            
            $step = 'done';
            $installLog[] = ['info', '── Installation complete!'];
        }
    }
