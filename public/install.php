function checkRequirements(): array {
    $checks = [];
    
    // PHP version
    $checks[] = [
        'label' => 'PHP Version ≥ 8.2',
        'ok'    => version_compare(PHP_VERSION, '8.2.0', '>='),
        'value' => PHP_VERSION,
    ];
    
    // Required extensions
    $extensions = ['pdo', 'pdo_mysql', 'json', 'mbstring', 'openssl', 'fileinfo', 'curl', 'tokenizer'];
    foreach ($extensions as $ext) {
        $checks[] = [
            'label' => "ext-{$ext}",
            'ok'    => extension_loaded($ext),
            'value' => extension_loaded($ext) ? 'Loaded' : 'Missing',
        ];
    }
    
    // Writable directories
    $dirs = ['storage/cache', 'storage/logs', 'storage/uploads'];
    foreach ($dirs as $dir) {
        $full = K2_ROOT . '/' . $dir;
        $writable = is_dir($full) && is_writable($full);
        $checks[] = [
            'label' => "{$dir}/ writable",
            'ok'    => $writable,
            'value' => $writable ? 'Writable' : (is_dir($full) ? 'Not writable' : 'Missing'),
        ];
    }
    
    // Root writable (for .env)
    $checks[] = [
        'label' => 'Project root writable (for .env)',
        'ok'    => is_writable(K2_ROOT),
        'value' => is_writable(K2_ROOT) ? 'Writable' : 'Not writable',
    ];
    
    // Vendor autoload
    $checks[] = [
        'label' => 'Composer dependencies installed',
        'ok'    => file_exists(K2_ROOT . '/vendor/autoload.php'),
        'value' => file_exists(K2_ROOT . '/vendor/autoload.php') ? 'Found' : 'Run: composer install',
    ];
    
    // Schema file
    $checks[] = [
        'label' => 'database/schema.sql exists',
        'ok'    => file_exists(K2_ROOT . '/database/schema.sql'),
        'value' => file_exists(K2_ROOT . '/database/schema.sql') ? 'Found' : 'Missing',
    ];
    
    return $checks;
}

function allRequirementsMet(array $checks): bool {
    foreach ($checks as $c) {
        if (!$c['ok']) return false;
    }
    return true;
}

function generateJwtSecret(): string {
